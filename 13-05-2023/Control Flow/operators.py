# arithmetic operators
# +, -, *, /, %, **, //
num1 = 10
num2 = 30
print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(num1 / num2)
print(num1 % num2)
print(num1 ** num2)
print(num1 // num2) 

# comparison operators 
# ==, !=, >, <, >=, <=
var1 = 10
var2 = 20
print(var1 == var2)
print(var1 != var2)
print(var1 > var2)
print(var1 < var2)
print(var1 >= var2)
print(var1 <= var2)

# assignment operators
# =, +=, -=, *=, /=, %=, //=, **=, &=, |=, ^=, >>=, <<=
sum = var1 + var2
print(sum)
sum += var1
print(sum)
sum -= var1
print(sum)
sum *= var1
print(sum)
sum /= var1
print(sum)


# logical operators
# and, or, not
input1 = True
input2 = False
print(input1 and input2)
print(input1 or input2)
print(not input1)
print(not input2)

